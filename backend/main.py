"""
MWS Invoice Parser — FastAPI application.

Routes (all mounted at root; Vite proxy strips /api prefix):
  POST /parse          — upload PDF, parse, validate, return rows + page images
  POST /save           — insert parsed rows into Supabase
  GET  /records/{site} — fetch saved records (supports start_date / end_date filters)
  GET  /export/{site}  — download .xlsx tracker in Melissa's exact format
"""

from __future__ import annotations

import base64
import os
import tempfile
from io import BytesIO
from typing import Any, Optional

from dotenv import load_dotenv
from fastapi import FastAPI, File, Form, HTTPException, UploadFile
from fastapi.middleware.cors import CORSMiddleware
from fastapi.responses import StreamingResponse
from pydantic import BaseModel

load_dotenv()

app = FastAPI(
    title="MWS Invoice Parser API",
    description="Parses Enbridge CNG and Elexicon electricity PDF invoices for Miller Waste Systems.",
    version="0.1.0",
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=[
        "http://localhost:5173",
        "https://mws-invoice-parser.vercel.app",
        "https://mws-invoice-parser-skormzys-projects.vercel.app",
    ],
    allow_origin_regex=r"https://mws-invoice-parser-.*\.vercel\.app",
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ─────────────────────────────────────────────────────────────
# Constants
# ─────────────────────────────────────────────────────────────

_TABLE_MAP = {
    "cambridge": "cambridge_invoices",
    "pickering_cng": "pickering_cng_invoices",
    "walgreen": "walgreen_invoices",
    "pickering_elexicon": "pickering_elexicon_invoices",
}

_ORDER_COL = {
    "cambridge": "start_date",
    "pickering_cng": "start_date",
    "walgreen": "start_date",
    "pickering_elexicon": "bill_period",
}

_SITE_NAMES = {
    "cambridge": "Cambridge_CNG",
    "pickering_cng": "Pickering_CNG",
    "walgreen": "Walgreen_CNG",
    "pickering_elexicon": "Pickering_Elexicon",
}

# Fields auto-generated by Supabase — strip before insert
_AUTO_FIELDS = {"id", "created_at"}


# ─────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────

def _get_supabase():
    url = os.environ.get("SUPABASE_URL", "")
    key = os.environ.get("SUPABASE_SERVICE_KEY", "")
    if not url or not key:
        raise HTTPException(
            status_code=503,
            detail=(
                "Supabase is not configured. "
                "Set SUPABASE_URL and SUPABASE_SERVICE_KEY environment variables."
            ),
        )
    from supabase import create_client  # type: ignore[import]
    return create_client(url, key)


def _dispatch_parse(
    invoice_type: str, pdf_path: str, filename: str
) -> tuple[list[Any], list[Any]]:
    if invoice_type == "cambridge":
        from parsers.cambridge import parse_cambridge
        return parse_cambridge(pdf_path, filename)
    elif invoice_type == "pickering_cng":
        from parsers.pickering_enbridge import parse_pickering_enbridge
        return parse_pickering_enbridge(pdf_path, filename)
    elif invoice_type == "walgreen":
        from parsers.walgreen import parse_walgreen
        return parse_walgreen(pdf_path, filename)
    elif invoice_type == "pickering_elexicon":
        from parsers.elexicon import parse_elexicon
        return parse_elexicon(pdf_path, filename)
    else:
        raise HTTPException(status_code=400, detail=f"Unknown invoice_type: {invoice_type!r}")


def _pdf_to_images(pdf_path: str) -> list[str]:
    """Convert all PDF pages to base64-encoded JPEG strings. Returns [] if unavailable."""
    try:
        from pdf2image import convert_from_path  # type: ignore[import]

        images = convert_from_path(pdf_path, dpi=120)
        result: list[str] = []
        for img in images:
            buf = BytesIO()
            img.save(buf, format="JPEG", quality=82)
            result.append(base64.standard_b64encode(buf.getvalue()).decode())
        return result
    except Exception:
        return []


def _clean_row(row: dict[str, Any]) -> dict[str, Any]:
    """Strip auto-generated fields before inserting into Supabase."""
    return {k: v for k, v in row.items() if k not in _AUTO_FIELDS}


