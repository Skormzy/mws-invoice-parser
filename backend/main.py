@app.get("/health")
async def health():
    return {"status": "ok"}


@app.post("/parse")
async def parse(
    invoice_type: str = Form(...),
    file: UploadFile = File(...),
):
    """
    Parse an invoice PDF and return extracted rows + PDF page images.

    Multipart form fields:
      invoice_type — one of: cambridge | pickering_cng | walgreen | pickering_elexicon
      file         — the PDF file
    """
    if invoice_type not in _TABLE_MAP:
        raise HTTPException(status_code=400, detail=f"Unknown invoice_type: {invoice_type!r}")

    content = await file.read()

    with tempfile.NamedTemporaryFile(suffix=".pdf", delete=False) as tmp:
        tmp.write(content)
        tmp_path = tmp.name

    try:
        rows_pydantic, parse_warnings = _dispatch_parse(
            invoice_type, tmp_path, file.filename or ""
        )
        row_dicts = [r.model_dump(mode="json") for r in rows_pydantic]
        page_images = _pdf_to_images(tmp_path)
    except HTTPException:
        raise
    except Exception as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc
    finally:
        try:
            os.unlink(tmp_path)
        except OSError:
            pass

    # Post-parse validation (range checks, HST, date order)
    from validation import validate_rows
    val_warnings = validate_rows(invoice_type, row_dicts)

    all_warnings = [w.model_dump() for w in (parse_warnings + val_warnings)]

    return {
        "invoice_type": invoice_type,
        "rows": row_dicts,
        "warnings": all_warnings,
        "pdf_page_images": page_images,
    }


class SaveRequest(BaseModel):
    invoice_type: str
    rows: list[dict[str, Any]]


@app.post("/save")
async def save(request: SaveRequest):
    """
    Insert parsed rows into the correct Supabase table.

    Body: { invoice_type: string, rows: [...] }
    """
    table = _TABLE_MAP.get(request.invoice_type)
    if not table:
        raise HTTPException(status_code=400, detail=f"Unknown invoice_type: {request.invoice_type!r}")

    sb = _get_supabase()
    clean_rows = [_clean_row(row) for row in request.rows]

    try:
        response = sb.table(table).insert(clean_rows).execute()
        inserted = len(response.data) if response.data else len(clean_rows)
        return {"success": True, "inserted": inserted}
    except Exception as exc:
        err = str(exc)
        if "duplicate" in err.lower() or "23505" in err or "unique" in err.lower():
            raise HTTPException(
                status_code=409,
                detail=(
                    "Duplicate record: one or more billing periods already exist in the database. "
                    "Delete the existing records first, or deselect the duplicate rows."
                ),
            )
        raise HTTPException(status_code=500, detail=err) from exc


@app.get("/records/{site_id}")
async def get_records(
    site_id: str,
    start_date: Optional[str] = None,
    end_date: Optional[str] = None,
):
    """
    Return all saved records for a site, newest first.

    Query params (optional):
      start_date — ISO date, inclusive lower bound on the date ordering column
      end_date   — ISO date, inclusive upper bound
    """
    table = _TABLE_MAP.get(site_id)
    if not table:
        raise HTTPException(status_code=400, detail=f"Unknown site_id: {site_id!r}")

    sb = _get_supabase()
    order_col = _ORDER_COL[site_id]

    query = sb.table(table).select("*").order(order_col, desc=True)
    if start_date:
        query = query.gte(order_col, start_date)
    if end_date:
        query = query.lte(order_col, end_date)

    try:
        response = query.execute()
        return response.data
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc


@app.get("/export/{site_id}")
async def export_excel(site_id: str):
    """
    Download all records as an .xlsx file matching Melissa's tracker format.
    """
    table = _TABLE_MAP.get(site_id)
    if not table:
        raise HTTPException(status_code=400, detail=f"Unknown site_id: {site_id!r}")

    sb = _get_supabase()
    order_col = _ORDER_COL[site_id]

    try:
        response = sb.table(table).select("*").order(order_col, desc=False).execute()
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    from export.excel import export_to_excel

    excel_bytes = export_to_excel(site_id, response.data or [])
    filename = f"{_SITE_NAMES.get(site_id, site_id)}_Invoice_Tracker.xlsx"

    return StreamingResponse(
        BytesIO(excel_bytes),
        media_type="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )
